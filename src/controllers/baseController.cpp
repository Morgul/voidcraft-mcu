#include "baseController.h"

//----------------------------------------------------------------------------------------------------------------------

void BaseController::tick()
{
    unsigned long currentTS = micros();

    // If we've waited long enough for the loop interval to fire, call the `loop` function.
    if((currentTS - this->lastTS) / 1000 >= this->loopInterval)
    {
        this->loop();
        this->lastTS = micros();
    }
}

